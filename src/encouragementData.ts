// 获取指定等级的随机鼓励语句
export function getRandomEncouragement(level: number): string {
  // 7级以上共用等级7的语句
  const effectiveLevel = level >= 7 ? 7 : level;
  const encouragements = levelEncouragements[effectiveLevel] || levelEncouragements[1];
  const randomIndex = Math.floor(Math.random() * encouragements.length);
  return encouragements[randomIndex];
}

// 获取等级名称
export function getLevelName(level: number): string {
  if (level >= 7) return levelNames[7];
  return levelNames[level] || levelNames[1];
}
